    private void btnAceptar_tutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptar_tutorActionPerformed
        if(lPersona.getSelectedIndex() <= 0){
            JOptionPane.showMessageDialog(this, "Selecciona una persona");
            return;
        }

        int indice = lPersona.getSelectedIndex() - 1;
        personaSeleccionada = personas.get(indice);

        if (JOptionPane.showConfirmDialog(this, "¿Seleccionar a la persona " 
                + lPersona.getSelectedItem().toString() + "?") == JOptionPane.YES_OPTION) {

            // Habilitar campos de secretaria
            txtUsuario.setEnabled(true);
            jPasswordField1.setEnabled(true);
            Guardar.setEnabled(true);

            // Deshabilitar selección para no cambiar
            lPersona.setEnabled(false);
            btnAceptar_tutor.setEnabled(false);
        }
    }//GEN-LAST:event_btnAceptar_tutorActionPerformed

    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        if(personaSeleccionada == null){
            JOptionPane.showMessageDialog(this, "Selecciona una persona primero.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(txtUsuario.getText().trim().isEmpty() || jPasswordField1.getPassword().length == 0){
            JOptionPane.showMessageDialog(this, "Ingresa usuario y contraseña.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Secretaria nuevaSecretaria = new Secretaria();
        nuevaSecretaria.setPersonaId(personaSeleccionada);
        nuevaSecretaria.setUsuario(txtUsuario.getText().trim());
        nuevaSecretaria.setContrasena(new String(jPasswordField1.getPassword()));

        try {
            cSecretaria.create(nuevaSecretaria);
            JOptionPane.showMessageDialog(this, "Secretaria registrada correctamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);

            // Reset campos
            txtUsuario.setText("");
            jPasswordField1.setText("");
            lPersona.setEnabled(true);
            btnAceptar_tutor.setEnabled(true);
            Guardar.setEnabled(false);

            // Recargar personas nuevas
            cargarPersonasNuevas();
            personaSeleccionada = null;

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar secretaria: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_GuardarActionPerformed

