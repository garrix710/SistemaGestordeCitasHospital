    public void cargarPersonasNuevas() {
        lPersona.removeAllItems();
        lPersona.addItem(SELECCIONA);

        personas = cPersona.findPersonaEntities();

        for (Persona p : personas) {
            boolean tieneDoctor = cDoctor.findDoctorEntities().stream().anyMatch(d -> d.getPersonaId().equals(p));
            boolean tieneSecretaria = cSecretaria.findSecretariaEntities().stream().anyMatch(s -> s.getPersonaId().equals(p));
            boolean tieneAdmin = cAdmin.findAdministradorEntities().stream().anyMatch(a -> a.getPersonaId().equals(p));
            boolean tienePaciente = cPaciente.findPacienteEntities().stream().anyMatch(pa -> pa.getPersonaId().equals(p));

            if (!tieneDoctor && !tieneSecretaria && !tieneAdmin && !tienePaciente) {
                lPersona.addItem(p.getNombre() + " " + p.getApellido());
            }
        }
    }


