    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        String nombre = dNombre.getText().trim();
    String apellido = dApellido.getText().trim();
    String telefono = dTelefono.getText().trim();
    String email = dEmail.getText().trim();
    String direccion = dDireccion.getText().trim();

    // Obtener fecha del JDateChooser
    java.util.Date fechaUtil = dFechaChooser.getDate();

    if (nombre.isEmpty() || apellido.isEmpty() || fechaUtil == null || direccion.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.", "Campos Vacíos", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Convertir java.util.Date a java.sql.Date
    java.sql.Date fechaNacimiento = new java.sql.Date(fechaUtil.getTime());

    persona=new Persona();
        persona.setNombre(nombre);
        persona.setApellido(apellido);
        persona.setTelefono(telefono);
        persona.setEmail(email);
        persona.setFechanacimiento(fechaNacimiento);
        persona.setDireccion(direccion);

    try {
        cPersona.create(persona);
        personas.add(persona);
        mtp.fireTableDataChanged();

        // Limpiar campos
        dNombre.setText("");
        dApellido.setText("");
        dTelefono.setText("");
        dEmail.setText("");
        dDireccion.setText("");
        dFechaChooser.setDate(null);

        JOptionPane.showMessageDialog(this, "Persona agregada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    } catch (HeadlessException e) {
        JOptionPane.showMessageDialog(this, "Error al agregar la persona: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnAgregarActionPerformed

