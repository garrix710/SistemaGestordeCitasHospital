    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        // Obtener el índice de la fila seleccionada en la tabla
        int filaSeleccionada = tpersonas.getSelectedRow();

        // Verificar si hay alguna fila seleccionada
        if (filaSeleccionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Por favor, seleccione un tutor para eliminar.",
                "Ninguna selección",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        // Confirmar eliminación con el usuario
        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this,
            "¿Está seguro de que desea eliminar este tutor?",
            "Confirmar eliminación",
            javax.swing.JOptionPane.YES_NO_OPTION);

        // Si el usuario confirma la eliminación
        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            try {
                // Obtener el tutor seleccionado
                Persona tutorSeleccionado = personas.get(filaSeleccionada);

                // Obtener el ID del tutor (idtutor es un Integer según la clase TutorJpaController)
                Integer idPersona = tutorSeleccionado.getId();

                // Eliminar de la base de datos usando el ID
                cPersona.destroy(idPersona);

                // Eliminar de la lista local
                personas.remove(filaSeleccionada);

                // Actualizar la tabla
                mtp.fireTableDataChanged();

                // Mostrar mensaje de éxito
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Tutor eliminado correctamente.",
                    "Éxito",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);

            } catch (NonexistentEntityException e) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Error: El tutor ya no existe en la base de datos.",
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
                // Actualizar la tabla de todas formas
                personas.remove(filaSeleccionada);
                mtp.fireTableDataChanged();
            } catch (HeadlessException e) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Error al eliminar el tutor: " + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


