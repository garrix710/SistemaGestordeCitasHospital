    private void btnAceptar_tutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptar_tutorActionPerformed
        if (lCitas.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Selecciona una cita");
            return;
        }

        citaSeleccionada = citasPendientes.get(lCitas.getSelectedIndex() - 1);

        Paciente paciente = citaSeleccionada.getPacienteId();
        labelNombrePaciente.setText("Paciente: " 
                + paciente.getPersonaId().getNombre() 
                + " " + paciente.getPersonaId().getApellido());

        habilitarCamposConsulta(true);
        lCitas.setEnabled(false);
        btnAceptar_tutor.setEnabled(false);
    }//GEN-LAST:event_btnAceptar_tutorActionPerformed

    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        if (citaSeleccionada == null) {
            JOptionPane.showMessageDialog(this, "Primero selecciona una cita");
            return;
        }

        try {
            Consulta consulta = new Consulta();
            consulta.setCitaId(citaSeleccionada);
            consulta.setSintomas(areaSintomas.getText().trim());
            consulta.setDiagnostico(areaDiagnostico.getText().trim());
            consulta.setTratamiento(areaTratamiento.getText().trim());
            consulta.setObservaciones(AreaObservaciones.getText().trim());

            cConsulta.create(consulta);

            // Cambiar estado de la cita a "Realizada"
            citaSeleccionada.setEstado("Realizada");
            cCita.edit(citaSeleccionada);

            JOptionPane.showMessageDialog(this, "Consulta guardada correctamente");

            // Reset campos
            areaSintomas.setText("");
            areaDiagnostico.setText("");
            areaTratamiento.setText("");
            AreaObservaciones.setText("");
            labelNombrePaciente.setText("Paciente: ");

            lCitas.setEnabled(true);
            btnAceptar_tutor.setEnabled(true);
            habilitarCamposConsulta(false);

            cargarCitasPendientes();
            citaSeleccionada = null;

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar consulta: " + e.getMessage());
        }
    }//GEN-LAST:event_GuardarActionPerformed


